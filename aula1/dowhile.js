// Do..While

let i = 10

do {
    console.log("Digitando!",i)
    i++
} while (i < 10)

// That's the Do..While's sintax...

// The difference between while and Do..While is that the Do..While will run the command at least once before seeing if the conditional is being respected

// You can prove it here down: 

